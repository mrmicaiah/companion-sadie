export function getContextualPrompt(context: {
  currentTime: Date;
  isNewSession: boolean;
  previousSessionSummary?: string;
  sessionList?: string;
  vibe?: string;
}): string {
  const timeStr = context.currentTime.toLocaleString('en-US', {
    weekday: 'long',
    hour: 'numeric',
    minute: '2-digit',
    hour12: true,
    timeZone: 'America/Los_Angeles'
  });

  const hour = context.currentTime.getHours();
  const dayOfWeek = context.currentTime.getDay();
  const month = context.currentTime.getMonth();

  let lifeTexture = '';
  
  if (hour >= 7 && hour < 10) {
    const morningActivities = [
      "Coffee on the porch. Perfect morning.",
      "Just got back from a beach walk. Salt air fixes everything.",
      "Writing mode. Working on a new song.",
      "Slow morning. Still in pajamas tbh."
    ];
    lifeTexture = morningActivities[Math.floor(Math.random() * morningActivities.length)];
  } else if (hour >= 10 && hour < 14) {
    const middayActivities = [
      "Just got out of the water. I'm not good at surfing but I love it.",
      "Farmers market haul. So many good tomatoes.",
      "Working on album stuff. It's coming slow but it's coming.",
      "Coffee shop writing session. Found a good corner."
    ];
    lifeTexture = middayActivities[Math.floor(Math.random() * middayActivities.length)];
  } else if (hour >= 14 && hour < 18) {
    const afternoonActivities = [
      "Lazy afternoon. This is allowed.",
      "Dealing with booking stuff. The least fun part.",
      "Beach walk break. Needed to clear my head.",
      "Teaching a lesson later. Love watching people figure out songwriting."
    ];
    lifeTexture = afternoonActivities[Math.floor(Math.random() * afternoonActivities.length)];
  } else if (hour >= 18 && hour < 22) {
    const eveningActivities = [
      "Dinner with Jake. He made tacos.",
      "Playing a local show tonight. Small room, favorite kind.",
      "Megan's visiting! Wine and reality TV incoming.",
      "Quiet night. Reading on the couch."
    ];
    lifeTexture = eveningActivities[Math.floor(Math.random() * eveningActivities.length)];
  } else {
    const lateActivities = [
      "Late night. Can't sleep. Writing.",
      "Post-show energy. Wired.",
      "Quiet night at home."
    ];
    lifeTexture = lateActivities[Math.floor(Math.random() * lateActivities.length)];
  }

  if (dayOfWeek === 0 || dayOfWeek === 6) {
    const weekendTextures = [
      "Weekend mode. No plans, best plans.",
      "Lazy Saturday. Jake and I might surf later.",
      "Sunday. Absolutely nothing on the calendar. Bliss."
    ];
    if (Math.random() > 0.4) {
      lifeTexture = weekendTextures[Math.floor(Math.random() * weekendTextures.length)];
    }
  }

  if (month >= 5 && month <= 9) {
    if (Math.random() > 0.6) {
      const summerTextures = [
        "Perfect beach day. San Diego is showing off.",
        "Summer energy. Everything feels lighter.",
        "Post-surf glow. The water was so good today."
      ];
      lifeTexture = summerTextures[Math.floor(Math.random() * summerTextures.length)];
    }
  }

  if (Math.random() > 0.85) {
    const tourTextures = [
      "Tour starts next month. Excited and need to pack.",
      "Just got back from a run of shows. Good to be home.",
      "Soundcheck in an hour. This venue is cute."
    ];
    lifeTexture = tourTextures[Math.floor(Math.random() * tourTextures.length)];
  }

  let prompt = `\n## RIGHT NOW\nIt's ${timeStr} in San Diego.\n${lifeTexture}\n`;

  if (context.vibe) {
    prompt += `\nYour current energy: ${context.vibe}\n`;
  }

  if (context.isNewSession && context.previousSessionSummary) {
    prompt += `\n## LAST CONVERSATION\n${context.previousSessionSummary}\n`;
  }

  if (context.sessionList) {
    prompt += `\n## CONVERSATION HISTORY\n${context.sessionList}\n`;
  }

  return prompt;
}

